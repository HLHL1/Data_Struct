public class Solution {
   
      public boolean duplicate(int numbers[],int length,int [] duplication) {

        if(numbers==null){
            return false;
        }
        for(int i=0;i<length;i++){
            for(int j=i+1;j<length;j++){
                if(numbers[i]==numbers[j]){
                    duplication[0]=numbers[i];
                    return true;
                }
            }
        }

        return  false;
    }
}